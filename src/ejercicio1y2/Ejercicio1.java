package ejercicio1y2;

public class Ejercicio1 {

    public static boolean esIgual (int a, int b) {
        boolean flag = false;
        if (a==b){
            flag =true;
            return flag;
        };
        return flag;
    }

    public static int hallarMayor (int a, int b){
        if (a>b){
            return a;
        }
        else {
            return b;
        }
    }
}
